package com.hamza.veterinarysystemdemo.StoreSection.Model;

public class StoreHistoryModel {

    public int id;
    public String image,name,phone,address;

    public StoreHistoryModel(int id, String image, String name, String phone, String address) {
        this.id = id;
        this.image = image;
        this.name = name;
        this.phone = phone;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
